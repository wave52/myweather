CREATE TABLE base_info (
    id int(11) NOT NULL,
    city_id VARCHAR(11) NULL,
    date_id VARCHAR(11) NULL,
    base_describe VARCHAR(255) NULL,
    now_temp int(11) NULL,
    max_temp int(11) NULL,
    min_temp int(11) NULL,
    humidity int(11) NULL,
    rain int(11) NULL,
    port_id int(11) NULL
);